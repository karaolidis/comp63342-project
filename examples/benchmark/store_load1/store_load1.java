class store_load1 {
  public static void test() {
    int a0 = 0;
    int a1 = 1;
    int a2 = 2;
    int a3 = 3;
    int a4 = 4;
    assert 0 == a0;
    assert 1 == a1;
    assert 2 == a2;
    assert 3 == a3;
    assert 4 == a4;

    long a0L = 0L;
    long a1L = 1L;
    long a2L = 2L;
    long a3L = 3L;
    long a4L = 4L;
    assert 0L == a0L;
    assert 1L == a1L;
    assert 2L == a2L;
    assert 3L == a3L;
    assert 4L == a4L;

    double a0D = 0.0;
    double a1D = 1.0;
    double a2D = 2.0;
    double a3D = 3.0;
    double a4D = 4.0;
    assert 0.0 == a0D;
    assert 1.0 == a1D;
    assert 2.0 == a2D;
    assert 3.0 == a3D;
    assert 4.0 == a4D;

    float a0F = 0.5f;
    float a1F = 1.0f;
    float a2F = 2.0f;
    float a3F = 3.0f;
    float a4F = 4.0f;
    assert 0.5f == a0F;
    assert 1.0f == a1F;
    assert 2.0f == a2F;
    assert 3.0f == a3F;
    assert 4.0f == a4F;
  }
}
